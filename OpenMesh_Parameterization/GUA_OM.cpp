double area(OMT::Point A,OMT::Point B,OMT::Point C)
{
	double l1,l2,l3;
	double s;
	l1 = (A-B).length();
	l2 = (B-C).length();
	l3 = (A-C).length();
	s = (l1+l2+l3)/2;
	return sqrt(s*(s-l1)*(s-l2)*(s-l3));
}
